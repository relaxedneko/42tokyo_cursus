#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ch_s;
	size_t	i;

	ch_s = (char *)s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		ch_s[i] = '\0';
		i++;
	}
}
